public class Math<x, y> {


    public static Integer modulus(int x , int y) {
         return  x % y;


    }

    public static Integer multiply(int a, int b) {
        return a * b;
    }
    public static boolean isGreaterThan(int e, int f) {
        return e > f;
    }
    public static boolean isLesserThan(int x, int y) {
        return x > y;
    }

    public static boolean equals(int x, int y) {
        return x == y;
    }

}
